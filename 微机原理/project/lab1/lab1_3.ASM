DATA SEGMENT
	XL	DW  0FFFFH	;X低16位
	XH	DW  0FFE0H	;X高16位
	YL	DW  00FFFH	;Y低16位
	YH	DW  0FF01H	;Y高16位
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
	MOV DS,AX

	MOV AX,XL		;X低16位移到AX
	ADD AX,YL		;低16位无符号数相加
	MOV DI,3600H	;偏移地址送给DI
	MOV [DI],AX		;已经计算的低16位结果送给3600H、3601H
	
	MOV AX,XH		;X高16位移到AX
	ADC AX,YH		;高16位相加，视作带进位的相加，可能有进位、溢出等
	MOV DI,3602H	;低16位相加存入之后偏移地址已经+2
	MOV [DI],AX		;高16位结果送给3602H、3603H
CODE	ENDS
	END  START